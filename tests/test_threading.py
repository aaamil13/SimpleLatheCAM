def test_gcode_writer_threading_produces_g76():
    op = _threading_op(pitch=1.5, length_z=20.0)
    profile = _build_profile_with_threading(tag=(0, 0), z_start=0.0, z_end=-20.0)
    writer = _make_writer(op, profile)
    lines = writer.generate_lines()
    g76_lines = [l for l in lines if "G76" in l]
    assert len(g76_lines) == 1, f"Expected exactly one G76 line, got: {g76_lines}"


def test_gcode_writer_threading_pitch_in_g76():
    op = _threading_op(pitch=2.0)
    profile = _build_profile_with_threading(tag=(0, 0))
    writer = _make_writer(op, profile)
    lines = writer.generate_lines()
    g76 = next(l for l in lines if "G76" in l)
    assert "P2.0000" in g76, f"Pitch P2.0000 not found in: {g76}"


def test_gcode_writer_threading_z_end_in_g76():
    op = _threading_op(pitch=1.5, length_z=30.0)
    profile = _build_profile_with_threading(tag=(0, 0), z_start=0.0, z_end=-30.0)
    writer = _make_writer(op, profile)
    lines = writer.generate_lines()
    g76 = next(l for l in lines if "G76" in l)
    assert "Z-30.000" in g76, f"Z-30.000 not found in: {g76}"


def test_gcode_writer_threading_approach_rapid():
    """Must have G0 rapid approach before G76."""
    op = _threading_op()
    profile = _build_profile_with_threading(tag=(0, 0))
    writer = _make_writer(op, profile)
    lines = writer.generate_lines()
    thread_idx = next(i for i, l in enumerate(lines) if "threading" in l)
    g0_before = [l for l in lines[thread_idx:thread_idx + 3] if l.startswith("G0")]
    assert g0_before, "Expected G0 rapid before G76"


def test_gcode_writer_threading_retract_after():
    """Must have G0 retract to safe X after G76."""
    op = _threading_op()
    profile = _build_profile_with_threading(tag=(0, 0))
    writer = _make_writer(op, profile)
    lines = writer.generate_lines()
    g76_idx = next(i for i, l in enumerate(lines) if "G76" in l)
    assert lines[g76_idx + 1].startswith("G0"), "Expected G0 retract after G76"


def test_gcode_writer_no_g76_without_threading_op():
    """Recipe without threading must not produce any G76 line."""
    from domain.tool_library import ToolLibrary
    profile = LatheProfile(stock_d=40.0, stock_l=100.0)
    profile.add_cylinder(d=30.0, length=50.0)
    stock = StockSpec(diameter=40.0, length=100.0)
    seq = ToolSequence(
        tool_id=1, coolant_on=False,
        operations=[OperationRecord("cylinder", {"d": 30.0, "length": 50.0})],
    )
    recipe = PartRecipe(stock=stock, tool_sequences=[seq])
    writer = GCodeWriter(recipe, profile, ToolLibrary.empty())
    lines = writer.generate_lines()
    assert not any("G76" in l for l in lines)


def test_gcode_writer_threading_auto_depth():
    """depth=0 should auto-calculate depth as 0.6495 * pitch."""
    op = _threading_op(pitch=1.5, depth=0.0, first_pass=0.3)
    profile = _build_profile_with_threading(tag=(0, 0))
    writer = _make_writer(op, profile)
    lines = writer.generate_lines()
    g76 = next(l for l in lines if "G76" in l)
    expected_depth = 0.6495 * 1.5  # = 0.97425
    assert f"K{expected_depth:.3f}" in g76, (
        f"Expected K{expected_depth:.3f} in: {g76}"
    )
