"""
Tests for threading primitive (plugins/primitives/threading.py)
and the G76 block generated by cam/gcode_writer._threading_block().
"""

import sys
from pathlib import Path
import pytest

ROOT = Path(__file__).parent.parent
sys.path.insert(0, str(ROOT))

from domain.profile import LatheProfile
from domain.recipe import OperationRecord, PartRecipe, StockSpec, ToolSequence
from domain.tool import SpindleMode, ToolDirection
from cam.gcode_writer import GCodeWriter, WriterConfig


# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

def _threading_op(pitch=1.5, length_z=20.0, depth=0.0, first_pass=0.3,
                  spring_passes=1.0, infeed_angle=29.5,
                  direction=ToolDirection.EXTERNAL) -> OperationRecord:
    return OperationRecord(
        primitive_name="threading",
        params={
            "pitch": pitch,
            "length_z": length_z,
            "z_offset": 0.0,
            "depth": depth,
            "first_pass": first_pass,
            "spring_passes": spring_passes,
            "infeed_angle": infeed_angle,
        },
        direction=direction,
    )


def _build_profile_with_threading(tag=(0, 0), thread_d=30.0,
                                   z_start=0.0, z_end=-20.0) -> LatheProfile:
    """Manually build a profile with threading segments pre-tagged."""
    profile = LatheProfile(stock_d=40.0, stock_l=100.0)
    profile.active_tag = tag
    profile.add_line_to(d=thread_d, z=z_start)
    profile.add_line_to(d=thread_d, z=z_end)
    return profile


def _make_writer(op: OperationRecord, profile: LatheProfile) -> GCodeWriter:
    from domain.tool_library import ToolLibrary
    stock = StockSpec(diameter=40.0, length=100.0)
    seq = ToolSequence(
        tool_id=1,
        spindle_mode=SpindleMode.CSS,
        spindle_value=200.0,
        max_rpm=2000,
        coolant_on=False,
        operations=[op],
    )
    recipe = PartRecipe(part_name="test", stock=stock, tool_sequences=[seq])
    return GCodeWriter(recipe, profile, ToolLibrary.empty())


