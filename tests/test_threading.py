def _loader():
    from domain.plugin_loader import PrimitivePluginLoader
    loader = PrimitivePluginLoader()
    loader.load(ROOT / "plugins" / "primitives")
    return loader


def test_threading_primitive_loads():
    """Plugin loader must find and instantiate the threading primitive."""
    prim = _loader().get("threading")
    assert prim is not None
    assert prim.name == "threading"


def test_threading_params_schema():
    prim = _loader().get("threading")
    names = [p.name for p in prim.params_schema]
    assert "pitch" in names
    assert "length_z" in names
    assert "depth" in names
    assert "first_pass" in names
    assert "spring_passes" in names
    assert "infeed_angle" in names


def test_threading_build_does_not_change_diameter():
    """Threading must not change the nominal surface diameter."""
    from domain.profile import LatheProfile

    prim = _loader().get("threading")
    profile = LatheProfile(stock_d=40.0, stock_l=100.0)
    profile.add_cylinder(d=30.0, length=5.0)
    x_before = profile.cursor_x

    prim.build(profile, {
        "pitch": 1.5, "length_z": 20.0, "z_offset": 0.0,
        "depth": 0.0, "first_pass": 0.3, "spring_passes": 1.0,
        "infeed_angle": 29.5,
    })

    assert abs(profile.cursor_x - x_before) < 1e-6, (
        "Threading must not change the surface diameter"
    )


def test_threading_build_advances_cursor_z():
    from domain.profile import LatheProfile

    prim = _loader().get("threading")
    profile = LatheProfile(stock_d=40.0, stock_l=100.0)
    profile.add_cylinder(d=30.0, length=5.0)
    z_before = profile.cursor_z

    prim.build(profile, {
        "pitch": 1.5, "length_z": 20.0, "z_offset": 0.0,
        "depth": 0.0, "first_pass": 0.3, "spring_passes": 1.0,
        "infeed_angle": 29.5,
    })

    assert profile.cursor_z < z_before, "cursor_z must advance in -Z direction"
    assert abs(profile.cursor_z - (z_before - 20.0)) < 1e-6


def test_threading_validate_rejects_large_first_pass():
    from domain.primitive_base import ProfileContext

    prim = _loader().get("threading")
    ctx = ProfileContext(cursor_x=30.0, cursor_z=0.0, stock_d=40.0, stock_l=100.0, segment_count=1)
